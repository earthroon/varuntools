#!/usr/bin/env node
import fs from 'node:fs'
import path from 'node:path'
const root = process.cwd()
const checks = []
const failures = []
const read = (file) => fs.readFileSync(path.join(root, file), 'utf8')
const exists = (file) => fs.existsSync(path.join(root, file))
function check(name, condition) { checks.push(name); if (!condition) failures.push(name) }
function includes(file, token) { return exists(file) && read(file).includes(token) }
const appliedPages = ['src/content/pages/works/index.md', 'src/content/pages/works/varuntools-showroom/index.md']
const requiredFiles = [...appliedPages, 'src/markdown/__fixtures__/portfolio-editorial-applied.md', 'docs/authoring/portfolio-editorial-usage-examples.md', 'docs/migration/commit-122.md', 'BAKE_REPORT_COMMIT_122.md']
for (const file of requiredFiles) check(`${file} exists`, exists(file))
const appliedContent = appliedPages.map((file) => read(file)).join('\n')
const fixture = read('src/markdown/__fixtures__/portfolio-editorial-applied.md')
const docs = read('docs/authoring/portfolio-editorial-usage-examples.md')
const migration = read('docs/migration/commit-122.md')
const report = read('BAKE_REPORT_COMMIT_122.md')
check('at least one real portfolio page uses editorial-title', appliedContent.includes('::editorial-title'))
check('at least one real portfolio page uses editorial-columns', appliedContent.includes('::editorial-columns'))
check('major editorial heading applied', appliedContent.includes('level: major'))
check('middle editorial heading applied', appliedContent.includes('level: middle'))
check('minor editorial heading applied', appliedContent.includes('level: minor'))
check('2-column pattern applied', appliedContent.includes('cols: 2'))
check('3-column pattern applied', appliedContent.includes('cols: 3'))
check('emotion structure technology pattern applied', appliedContent.includes('### 감정') && appliedContent.includes('### 구조') && appliedContent.includes('### 기술'))
check('problem solution pattern applied', appliedContent.includes('### 문제') && appliedContent.includes('### 해결'))
check('existing markdown headings remain in applied content', appliedContent.includes('# Works') && appliedContent.includes('# VARUNTOOLS Showroom System'))
check('fixture contains editorial-title', fixture.includes('::editorial-title'))
check('fixture contains editorial-columns', fixture.includes('::editorial-columns'))
check('fixture contains major heading', fixture.includes('level: major'))
check('fixture contains middle heading', fixture.includes('level: middle'))
check('fixture contains minor heading', fixture.includes('level: minor'))
check('fixture contains 2-column example', fixture.includes('cols: 2'))
check('fixture contains 3-column example', fixture.includes('cols: 3'))
check('fixture preserves markdown heading statement', fixture.includes('# Applied editorial blocks remain layered on top of Markdown'))
check('usage doc explains existing markdown headings stay valid', docs.includes('Existing Markdown headings stay valid'))
check('usage doc includes editorial-title', docs.includes('editorial-title'))
check('usage doc includes editorial-columns', docs.includes('editorial-columns'))
check('usage doc includes major heading', docs.includes('Major heading'))
check('usage doc includes middle heading', docs.includes('Middle heading'))
check('usage doc includes minor heading', docs.includes('Minor heading'))
check('usage doc includes 2-column pattern', docs.includes('2-column pattern'))
check('usage doc includes 3-column pattern', docs.includes('3-column pattern'))
check('usage doc includes overuse rule', docs.includes('Overuse rule'))
check('usage doc includes accessibility rule', docs.includes('Accessibility rule'))
check('migration doc states renderer not redesigned', migration.includes('without redesigning the renderer'))
check('migration doc states inquiry not modified', migration.includes('The inquiry system is not modified'))
check('bake report seals content application', report.includes('실제 포트폴리오 콘텐츠 적용 커밋'))
check('bake report lists applied pages', report.includes('src/content/pages/works/index.md') && report.includes('src/content/pages/works/varuntools-showroom/index.md'))
const packageJson = JSON.parse(read('package.json'))
check('package script exists', packageJson.scripts?.['smoke:portfolio-editorial-applied'] === 'node scripts/smoke-portfolio-editorial-applied.mjs')
check('check-launch includes applied editorial smoke', includes('scripts/check-launch.mjs', 'smoke-portfolio-editorial-applied.mjs'))
const sourceBundle = requiredFiles.concat(['package.json', 'scripts/check-launch.mjs']).map((file) => read(file)).join('\n')
check('source contains no [object Object]', !sourceBundle.includes('[object Object]'))
if (failures.length) { console.error('smoke:portfolio-editorial-applied FAILED'); for (const f of failures) console.error(`- ${f}`); process.exit(1) }
console.log(`[smoke:portfolio-editorial-applied] OK - ${checks.length} checks`)
